/**
 * 移位操作符  >>有符号右移 >>>无符号右移
 *
 * @author rankin_dean@163.com
 */
 
public class Ch3_11{
	public static void main(String[] args){
		int ii = 0x80808080;
		System.out.println(ii);
		int jj = ii;
		System.out.println("有符号右移操作");
		for(int i=0;i<32;i++){
			System.out.println(Integer.toBinaryString(ii));
			//有符号右移 负数补1
			ii >>= 1;
		}
		System.out.println("无符号右移操作");
		for(int j=0;j<32;j++){
			System.out.println(Integer.toBinaryString(jj));
			//无符号右移
			jj >>>= 1;
		}
	}
}
