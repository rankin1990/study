/**
 * 移位操作符  <<左移 >>>无符号右移
 *
 * @author rankin_dean@163.com
 */
 
public class Ch3_12{
	public static void main(String[] args){
		int ii = 0xFFFFFFFF;
		System.out.println(ii);
		System.out.println("左移操作");
		ii <<= 1;
		System.out.println(Integer.toBinaryString(ii));
		System.out.println("无符号右移操作");
		for(int j=0;j<32;j++){
			System.out.println(Integer.toBinaryString(ii));
			//无符号右移
			ii >>>= 1;
		}
	}
}
